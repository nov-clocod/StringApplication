package com.pluralsight;

public class VariableApp {
    public static void main(String[] args) {
        String favColor = "Black";
        short year = 2025;
        char middle_initial = 'M';
        boolean havePet = false;
        String message = "You look awesome today";

        System.out.println("My favorite color is: " + favColor);
        System.out.println("The year I started this class is: " + year);
        System.out.println("My middle middle_initial is: " + middle_initial);
        System.out.println("I have a pet: " + havePet);
        System.out.println("Nice message: " + message);

        byte weekDays = 7;
        String coffeePrice = "$4.99";
        char favLetter = 'A';
        boolean isRaining = false;

        System.out.println("The number of days in a week is: " + weekDays);
        System.out.println("The price of the coffee is: " + coffeePrice);
        System.out.println("My favorite letter is: " + favLetter);
        System.out.println("Is it raining today: " + isRaining);
    }
}
